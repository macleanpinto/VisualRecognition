package com.tmt.cognitive.challenge.visualrecognition.entity;

public class AdsMappingDetails {

	private Integer adsMappingId;
	private Integer userProfileId;
	private Integer productId;
	private String adsInfo;
	
	public Integer getAdsMappingId() {
		return adsMappingId;
	}
	public void setAdsMappingId(Integer adsMappingId) {
		this.adsMappingId = adsMappingId;
	}
	public Integer getUserProfileId() {
		return userProfileId;
	}
	public void setUserProfileId(Integer userProfileId) {
		this.userProfileId = userProfileId;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public String getAdsInfo() {
		return adsInfo;
	}
	public void setAdsInfo(String adsInfo) {
		this.adsInfo = adsInfo;
	}
	
}
