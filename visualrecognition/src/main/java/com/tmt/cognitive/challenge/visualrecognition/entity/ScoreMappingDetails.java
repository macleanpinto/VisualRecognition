package com.tmt.cognitive.challenge.visualrecognition.entity;

public class ScoreMappingDetails {

	private Integer scoreMappingId;
	private Integer userProfileId;
	private Integer productId;
	private Double score;
	
	public Integer getScoreMappingId() {
		return scoreMappingId;
	}
	public void setScoreMappingId(Integer scoreMappingId) {
		this.scoreMappingId = scoreMappingId;
	}
	public Integer getUserProfileId() {
		return userProfileId;
	}
	public void setUserProfileId(Integer userProfileId) {
		this.userProfileId = userProfileId;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
	
	
}
